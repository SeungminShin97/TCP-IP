package framework.core.exception;

/**
 * 예외 발생 시 프레임워크가 취해야 할 처리 방향을 정의하는 enum
 *
 * <p>
 * 예외 이후의 흐름 제어 결정을 표현하며,
 * 실제 중단 범위와 처리 방식은 이를 해석하는 상위 컴포넌트에 의해 결정된다.
 */
public enum ExceptionAction {

    /**
     * 현재 처리 중인 항목을 더 이상 진행하지 않고 폐기한다.
     * <p>
     * 네트워크 계층에서는 정상적인 손실 상황으로 간주될 수 있다.
     */
    DROP,

    /**
     * 현재 실행 단위를 더 이상 안전하게 진행할 수 없음을 나타낸다.
     * <p>
     * 실제 중단 범위(계층, 스레드, 파이프라인 등)는
     * 실행 전략에 따라 결정된다.
     */
    STOP
}
