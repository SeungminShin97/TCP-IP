package framework.core.exception;

/**
 * 예외 상황에 대해 프레임워크가 권장하는 로그 출력 수준을 나타낸다.
 *
 * <p>
 * 이 enum은 SLF4J의 로그 레벨을 직접 제어하기 위한 목적이 아니라,
 * 예외가 전달하는 의미적 중요도를 표현하기 위한 힌트로 사용된다.
 *
 * <p>
 * 실제 로그 출력 방식과 구현체(SLF4J 등)는
 * 이를 해석하는 상위 컴포넌트에서 결정한다.
 */
public enum LogLevel {

    /**
     * 디버깅 또는 개발 단계에서만 의미가 있는 정보성 메시지.
     * <p>
     * 운영 환경에서는 기본적으로 출력되지 않을 수 있다.
     */
    DEBUG,

    /**
     * 시스템의 정상적인 동작 흐름을 나타내는 정보성 메시지.
     * <p>
     * 상태 변화나 실행 시작/종료 시점 등에 사용될 수 있다.
     */
    INFO,

    /**
     * 시스템은 정상적으로 동작하고 있으나,
     * 관찰 또는 추적이 필요한 상황을 나타낸다.
     */
    WARN,

    /**
     * 시스템의 안정성이나 실행 흐름에 영향을 줄 수 있는
     * 심각한 오류 상황을 나타낸다.
     */
    ERROR
}
